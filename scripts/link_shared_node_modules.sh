#!/usr/bin/env bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
WORKTREE_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
COMMON_GIT_DIR="$(git -C "${WORKTREE_ROOT}" rev-parse --path-format=absolute --git-common-dir)"
SHARED_REPO_ROOT="$(cd "${COMMON_GIT_DIR}/.." && pwd)"

usage() {
  cat <<'EOF'
Usage:
  scripts/link_shared_node_modules.sh [worktree_path]

Description:
  Link shared node_modules from the main repository checkout into an existing
  git worktree for both services/aris-web and services/aris-backend.
  If the shared checkout is missing dev dependencies, bootstrap them first with
  npm ci --include=dev.
EOF
}

if [[ "${1:-}" == "-h" || "${1:-}" == "--help" ]]; then
  usage
  exit 0
fi

TARGET_ROOT_INPUT="${1:-$PWD}"
if [[ "${TARGET_ROOT_INPUT}" = /* ]]; then
  TARGET_ROOT="${TARGET_ROOT_INPUT}"
else
  TARGET_ROOT="${PWD%/}/${TARGET_ROOT_INPUT}"
fi

if [[ ! -e "${TARGET_ROOT}/.git" ]]; then
  echo "[link-shared-node-modules] target is not a git worktree: ${TARGET_ROOT}" >&2
  exit 1
fi

ensure_service_dependencies() {
  local service_path="$1"
  shift
  local service_root="${SHARED_REPO_ROOT}/${service_path}"
  local node_modules_dir="${service_root}/node_modules"
  local needs_bootstrap=0

  if [[ ! -d "${node_modules_dir}" ]]; then
    needs_bootstrap=1
  else
    local required_bin=""
    for required_bin in "$@"; do
      if [[ ! -x "${node_modules_dir}/.bin/${required_bin}" ]]; then
        needs_bootstrap=1
        break
      fi
    done
  fi

  if [[ "${needs_bootstrap}" -eq 0 ]]; then
    echo "[link-shared-node-modules] shared deps ready: ${service_root}"
    return
  fi

  echo "[link-shared-node-modules] bootstrapping shared deps in ${service_root}"
  (
    cd "${service_root}"
    env -u NODE_ENV npm ci --include=dev
  )
}

link_service_node_modules() {
  local service_path="$1"
  local source_dir="${SHARED_REPO_ROOT}/${service_path}/node_modules"
  local target_dir="${TARGET_ROOT}/${service_path}/node_modules"
  local target_parent
  target_parent="$(dirname "${target_dir}")"

  if [[ ! -d "${source_dir}" ]]; then
    echo "[link-shared-node-modules] source node_modules missing: ${source_dir}" >&2
    exit 1
  fi

  mkdir -p "${target_parent}"

  if [[ -L "${target_dir}" ]]; then
    local current_target
    current_target="$(readlink "${target_dir}")"
    if [[ "${current_target}" == "${source_dir}" ]]; then
      echo "[link-shared-node-modules] already linked: ${target_dir}"
      return
    fi
    echo "[link-shared-node-modules] conflicting symlink exists: ${target_dir} -> ${current_target}" >&2
    exit 1
  fi

  if [[ -e "${target_dir}" ]]; then
    echo "[link-shared-node-modules] existing path blocks symlink: ${target_dir}" >&2
    exit 1
  fi

  ln -s "${source_dir}" "${target_dir}"
  echo "[link-shared-node-modules] linked ${target_dir} -> ${source_dir}"
}

ensure_service_dependencies "services/aris-web" "tsc" "vitest"
ensure_service_dependencies "services/aris-backend" "tsc" "vitest"

link_service_node_modules "services/aris-web"
link_service_node_modules "services/aris-backend"
