#!/usr/bin/env bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
WORKTREE_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
COMMON_GIT_DIR="$(git -C "${WORKTREE_ROOT}" rev-parse --path-format=absolute --git-common-dir)"
SHARED_REPO_ROOT="$(cd "${COMMON_GIT_DIR}/.." && pwd)"
LINK_SCRIPT="${SCRIPT_DIR}/link_shared_node_modules.sh"

usage() {
  cat <<'EOF'
Usage:
  scripts/create_worktree_with_shared_node_modules.sh <worktree_path> <branch> [base_ref]

Description:
  Create a dedicated git worktree and automatically link shared node_modules
  for services/aris-web and services/aris-backend.
  The helper bootstraps missing shared dev dependencies before linking.
EOF
}

if [[ "${1:-}" == "-h" || "${1:-}" == "--help" ]]; then
  usage
  exit 0
fi

if [[ $# -lt 2 || $# -gt 3 ]]; then
  usage >&2
  exit 1
fi

WORKTREE_PATH_INPUT="$1"
BRANCH_NAME="$2"
BASE_REF="${3:-main}"

if [[ "${WORKTREE_PATH_INPUT}" = /* ]]; then
  WORKTREE_PATH="${WORKTREE_PATH_INPUT}"
else
  WORKTREE_PATH="${SHARED_REPO_ROOT%/}/${WORKTREE_PATH_INPUT}"
fi

git -C "${SHARED_REPO_ROOT}" worktree add "${WORKTREE_PATH}" -b "${BRANCH_NAME}" "${BASE_REF}"
"${LINK_SCRIPT}" "${WORKTREE_PATH}"

echo "[create-worktree-with-shared-node-modules] ready: ${WORKTREE_PATH}"
